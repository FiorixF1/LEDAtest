void printBytes(unsigned char *data, int length) {
    char buffer[16];
    int i;
    for (i = 0; i < length; ++i) {
        HAL_UART_Transmit(&huart3, (uint8_t*)buffer, sprintf(buffer, "%02X", data[i]), 0xFFFFFF);
    }
}
